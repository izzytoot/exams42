#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	r;
	
	i = 0;
	if (argc == 2)
	{
		while (argv[1][i])
		{
			i++;
		}
		i--;
		r = i;
		while (r >= 0)
		{
			write (1, &argv[1][i], 1);
			r--;
			i--;
		}
		argv[1][i] = '\0';
	}
	write (1, "\n", 1);
	return (0);
}
